from fastapi import Depends, FastAPI, HTTPException
from sqlalchemy.orm import Session

import crud, models, schemas
from database import SessionLocal, engine

models.Base.metadata.create_all(bind=engine)

app = FastAPI()


# Dependency
def get_db():
    db = SessionLocal()
    try:
        yield db
    finally:
        db.close()

#--- Возвращение книги по id
@app.get("/books/{book_id}", response_model=schemas.Book)
def read_book(book_id: int, db: Session = Depends(get_db)):
    db_book = crud.get_book(db, book_id=book_id)
    if db_book is None:
        raise HTTPException(status_code=404, detail="User not found")
    return db_book

@app.post("/books/", response_model=schemas.Book)
def create_book(book: schemas.BookCreate, author_ids: list[int], db: Session = Depends(get_db)):
    return crud.create_book(db=db, book=book, author_ids=author_ids)

#--- Создание книги
@app.post("/books/", response_model=schemas.Book)
def create_book(book: schemas.BookCreate, db: Session = Depends(get_db)):
    db_book = crud.get_book_by_title(db, title=book.title)
    if db_book:
        raise HTTPException(status_code=400, detail="Title already registered")
    return crud.create_book(db=db, book=book)

#--- Получение списка книг
@app.get("/books/", response_model=list[schemas.Book])
def read_books(skip: int = 0, limit: int = 100, db: Session = Depends(get_db)):
    books = crud.get_books(db, skip=skip, limit=limit)
    return books


#--- Создание автора
@app.post("/authors/", response_model=schemas.Author)
def create_author(author: schemas.AuthorCreate, db: Session = Depends(get_db)):
    db_author = crud.get_author_by_name(db, name=author.name, country=author.country)
    if db_author:
        raise HTTPException(status_code=400, detail="Title already registered")
    return crud.create_author(db=db, author=author)

#--- Возвращение автора по id
